/// OpenWeatherMap Day
public enum Day: String, Codable, CaseIterable {
    
    case sunday         = "Sun"
    case monday         = "Mon"
    case tuesday        = "Tue"
    case wednesday      = "Wed"
    case thursday       = "Thu"
    case friday         = "Fri"
    case saturday       = "Sat"
}
